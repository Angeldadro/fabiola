export const shareSite = async (text) => {
  const url = window.location.origin + "/";
  if (navigator.share) {
    try {
      await navigator.share({ title: "Dulce Café", text, url });
      return;
    } catch (e) {
      return;
    }
  }
  window.open(
    `https://wa.me/?text=${encodeURIComponent(text + " " + url)}`,
    "_blank",
    "noopener"
  );
};
